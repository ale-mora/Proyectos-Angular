
export class Empleado {
    numero: number;
    nombre: string;
    apellido: string;
    sexo: string;
    salario: number;
    
    constructor(numero: number, nombre: string, apellido: string, sexo: string, salario: number) {
        this.numero = numero;
        this.nombre = nombre;
        this.apellido= apellido;
        this.sexo= sexo;
        this.salario= salario;
    }
}

